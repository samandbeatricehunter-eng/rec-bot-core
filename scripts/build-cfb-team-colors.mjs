import { writeFile } from "node:fs/promises";
import { CFB_27_TEAMS } from "../packages/shared/dist/cfb-teams.js";

const response = await fetch("https://site.api.espn.com/apis/site/v2/sports/football/college-football/teams?limit=1000");
if (!response.ok) throw new Error(`ESPN team catalog failed: ${response.status}`);
const payload = await response.json();
const teams = payload.sports[0].leagues[0].teams.map((entry) => entry.team);
const aliases = { Pitt:"Pittsburgh",SMU:"Southern Methodist",Tulane:"Tulane",UTSA:"UT San Antonio",Purdue:"Purdue",TCU:"TCU", "Texas Tech":"Texas Tech",FIU:"Florida International","Missouri State":"Missouri State","Sam Houston":"Sam Houston",UTEP:"UTEP",Toledo:"Toledo",UMass:"Massachusetts","Western Michigan":"Western Michigan","San Jose State":"San José State",Wyoming:"Wyoming","South Carolina":"South Carolina",Tennessee:"Tennessee","Appalachian State":"App State","Southern Miss":"Southern Mississippi",Troy:"Troy",ULM:"UL Monroe" };
const norm = (value) => String(value ?? "").normalize("NFD").replace(/[\u0300-\u036f]/g, "").toUpperCase().replace(/[^A-Z0-9]/g, "");
const output = {};
for (const team of CFB_27_TEAMS) {
  if (team.isSchedulePlaceholder) {
    output[team.abbreviation] = "#00843D";
    continue;
  }
  const target = aliases[team.name] ?? team.name;
  const match = teams.find((candidate) => norm(candidate.abbreviation) === norm(team.abbreviation))
    ?? teams.find((candidate) => norm(candidate.location) === norm(target) || norm(candidate.displayName) === norm(`${target} ${team.mascot}`));
  if (!match?.color) throw new Error(`No primary color match for ${team.name} ${team.mascot} (${team.abbreviation})`);
  output[team.abbreviation] = `#${String(match.color).replace(/^#/, "").toUpperCase()}`;
}
const body = `// Generated by scripts/build-cfb-team-colors.mjs from ESPN's current team catalog.\nexport const CFB_TEAM_PRIMARY_COLORS: Readonly<Record<string, string>> = ${JSON.stringify(output, null, 2)};\n`;
await writeFile(new URL("../packages/shared/src/cfb-team-colors.generated.ts", import.meta.url), body);
console.log(`Generated ${Object.keys(output).length} CFB primary colors.`);
